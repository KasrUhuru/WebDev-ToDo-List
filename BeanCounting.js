function countBs(str) {
    bCount = 0;
    for (let i = 0; i < length.str; i++) {
        if (str[i] === 'B') {
            bCount+=1;
        }
    }
    console.log(bCount);
};

function countChar (str, x) {
    xCount = 0;
    for (let i = 0; i < length.str; i++) {
        if (str[i] === x) {
            xCount+=1
        }
    }
}